import asyncHandler from "express-async-handler";
import axios from "axios";
import supabase from "../config/supabase.js";
import { generatePayload } from "../utils/harness.js";
import { declareWinner } from "./match.controller.js";

const oneCompilerLanguageMap = {
  javascript: "nodejs",
  python: "python",
  cpp: "cpp",
  java: "java",
};

// @desc   Execute code on OneCompiler with Photo Finish logic (SUBMIT)
// @route  POST /api/code/execute
// @access Public
export const executeCode = asyncHandler(async (req, res) => {
  const submissionTime = Date.now();

  const { code, language, problemId, roomId, userId } = req.body;

  if (!code || !language || !problemId) {
    res.status(400);
    throw new Error("Please provide code, language, and problemId");
  }

  console.log(
    `⚡ Received ${language} submission from user ${userId || "Anonymous"} for ${problemId}`,
  );

  // FETCH TEST CASES AND METADATA FROM SUPABASE
  const { data: problem, error: dbError } = await supabase
    .from("problems")
    .select("metadata, test_cases")
    .eq("id", problemId)
    .single();

  if (dbError || !problem) {
    console.error("Database fetch error:", dbError.message);
    return res
      .status(404)
      .json({ success: false, error: "Problem not found in database." });
  }

  // GENERATE HARNESS CODE AND STDIN PAYLOAD
  const { sourceCode, stdin } = generatePayload(
    language,
    code,
    problem.metadata,
    problem.test_cases,
  );

  // 🐛 DEBUG LOG: Check the exact payload generated by the harness
  console.log("\n==================== FINAL PAYLOAD ====================");
  console.log("--- SOURCE CODE ---");
  console.log(sourceCode);
  console.log("--- STDIN ---");
  console.log(stdin);
  console.log("=======================================================\n");

  try {
    console.log("RapidAPI Key:", process.env.RAPIDAPI_KEY?.slice(0, 10));
    const response = await axios.post(
      "https://onecompiler-apis.p.rapidapi.com/api/v1/run",
      {
        language: oneCompilerLanguageMap[language] || language,
        stdin: stdin, // Injected dynamically generated test cases
        files: [
          {
            name: `index.${language === "python" ? "py" : language === "javascript" ? "js" : language === "cpp" ? "cpp" : "java"}`,
            content: sourceCode,
          },
        ],
      },
      {
        headers: {
          "content-type": "application/json",
          "X-RapidAPI-Key":
            process.env.RAPIDAPI_KEY || process.env.ONECOMPILER_API_KEY,
          "X-RapidAPI-Host": "onecompiler-apis.p.rapidapi.com",
        },
      },
    );

    const runResult = response.data;

    // 🐛 DEBUG LOG: Check the exact raw response from OneCompiler
    console.log("\n==================== API RESPONSE =====================");
    console.log(JSON.stringify(runResult, null, 2));
    console.log("=======================================================\n");

    if (runResult.status !== "success" && !runResult.stdout) {
      return res.json({
        success: false,
        error: runResult.exception || runResult.stderr || "Execution Error",
      });
    }

    const stdout = runResult.stdout ? runResult.stdout.trim() : "";

    // PARSE DETERMINISTIC TOKEN EMITTED BY THE C++ BINARY
    const resultMatch = stdout.match(/RESULT\|(\d+)\/(\d+)/);

    if (resultMatch) {
      const passedCases = parseInt(resultMatch[1]);
      const totalCases = parseInt(resultMatch[2]);
      const allPassed = passedCases === totalCases;

      if (allPassed && roomId) {
        const executionTimeMs = Date.now() - submissionTime;
        const matchResult = await declareWinner(
          roomId,
          userId,
          problemId,
          executionTimeMs,
        );

        if (matchResult.success && global.io) {
          global.io.to(roomId).emit("match_over", {
            winnerId: userId,
            pointsExchanged: matchResult.pointsExchanged,
          });
        }
      }

      res.status(200).json({
        success: true,
        passedCount: passedCases,
        totalCount: totalCases,
        allPassed: allPassed,
        executionTimeMs: Date.now() - submissionTime,
      });
    } else {
      res.status(200).json({
        success: false,
        error: "Could not parse execution result. Did your code crash?",
        details: runResult.stderr || stdout,
      });
    }
  } catch (error) {
  console.log("========== EXECUTE ERROR ==========");
  console.log("Status:", error.response?.status);
  console.log("Data:", error.response?.data);
  console.log("Headers:", error.response?.headers);

  return res.status(error.response?.status || 500).json({
    success: false,
    error: error.response?.data || error.message,
  });
}

});
// @desc   Execute code against SAMPLE test cases only (RUN)
// @route  POST /api/code/run
// @access Public
export const runSampleCode = asyncHandler(async (req, res) => {
  const { code, language, problemId } = req.body;

  if (!code || !language || !problemId) {
    res.status(400);
    throw new Error("Please provide code, language, and problemId");
  }

  // FETCH METADATA AND TEST CASES
  const { data: problem, error: dbError } = await supabase
    .from("problems")
    .select("metadata, test_cases")
    .eq("id", problemId)
    .single();

  if (dbError || !problem) {
    return res.status(404).json({ success: false, error: "Problem not found" });
  }

  // Grab only the sample test cases (fallback to first 3 if not explicitly marked)
  const sampleCases = problem.test_cases.filter(tc => tc.is_example || tc.isExample);
  const casesToRun = sampleCases.length > 0 ? sampleCases : problem.test_cases.slice(0, 3);

  // GENERATE HARNESS (Same as submit, but fewer cases)
  const { sourceCode, stdin } = generatePayload(
    language,
    code,
    problem.metadata,
    casesToRun
  );

  try {
    console.log("RapidAPI Key:", process.env.RAPIDAPI_KEY?.slice(0, 10));
    const response = await axios.post(
      "https://onecompiler-apis.p.rapidapi.com/api/v1/run",
      {
        language: oneCompilerLanguageMap[language] || language,
        stdin: stdin,
        files: [{ 
          name: `index.${language === "python" ? "py" : language === "javascript" ? "js" : language === "cpp" ? "cpp" : "java"}`, 
          content: sourceCode 
        }],
      },
      {
        headers: {
          "content-type": "application/json",
          "X-RapidAPI-Key": process.env.RAPIDAPI_KEY || process.env.ONECOMPILER_API_KEY,
          "X-RapidAPI-Host": "onecompiler-apis.p.rapidapi.com",
        },
      }
    );

    const runResult = response.data;
    let stdout = runResult.stdout ? runResult.stdout.trim() : "";
    let stderr = runResult.stderr || runResult.exception || "";

    let passedCount = 0;
    const totalCount = casesToRun.length;

    // PARSE PASSED COUNT & STRIP THE HARNESS TOKEN
    const resultMatch = stdout.match(/RESULT\|(\d+)\/(\d+)/);
    if (resultMatch) {
      passedCount = parseInt(resultMatch[1]);
      // Remove our internal RESULT string so the user only sees their own console logs
      stdout = stdout.replace(/\n?RESULT\|\d+\/\d+\n?/, '').trim();
    }

    res.status(200).json({
      success: true,
      passedCount,
      totalCount,
      allPassed: passedCount === totalCount,
      stdout,
      stderr,
    });

  } catch (error) {
  console.log("========== RUN ERROR ==========");
  console.log("Status:", error.response?.status);
  console.log("Data:", error.response?.data);
  console.log("Headers:", error.response?.headers);

  return res.status(error.response?.status || 500).json({
    success: false,
    error: error.response?.data || error.message, });
  
  }
});